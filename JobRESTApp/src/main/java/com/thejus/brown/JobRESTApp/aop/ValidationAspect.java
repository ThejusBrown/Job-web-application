package com.thejus.brown.JobRESTApp.aop;

import org.aspectj.lang.ProceedingJoinPoint;
import org.aspectj.lang.annotation.Around;
import org.aspectj.lang.annotation.Aspect;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.stereotype.Component;

@Component
@Aspect
public class ValidationAspect {
    private  static final Logger LOGGER= LoggerFactory.getLogger(ValidationAspect.class);
    @Around("execution(* com.thejus.brown.JobRESTApp.service.Service.*(..))&& args(postId)")
    public Object validateAndUpdate(ProceedingJoinPoint jp,int postId) throws Throwable {
        if(postId<0){
            LOGGER.info("PostId is negative "+postId+" ,Updating it ");
            postId=-postId;
            LOGGER.info("PostId has updated to "+ postId);
        }
        Object obj=jp.proceed(new   Object[]{postId});

        return obj;
    }
}
